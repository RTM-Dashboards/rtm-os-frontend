// ── Convert an old Opportunity to an OpportunityRecord ───────────────────────
// Maps fields from the Kanban Opportunity type to the unified OpportunityRecord.

function toRecord(opp) {
  const now = new Date().toISOString();
  return {
    // ── Core OpportunityRecord fields ──
    id: opp.id,
    opportunityNumber: `OPP-2025-K${opp.id.replace("O", "")}`,
    leadId: null,
    clientName: opp.clientName,
    businessName: opp.businessName,
    tradeType: opp.industry ?? "",
    contactName: opp.primaryContact ?? opp.clientName,
    contactPhone: opp.phone ?? "",
    contactEmail: opp.email ?? "",
    leadSource: opp.leadSource,
    assignedRep: opp.assignedRep,
    stage: opp.stage,
    priority: opp.priority,
    estimatedMonthlyValue: opp.estimatedValue ?? opp.monthlyValue ?? 0,
    expectedCloseDate: opp.expectedCloseDate ?? "",
    serviceInterest: [],
    discoveryNotes: opp.notes?.[0]?.note ?? "",
    ghlContactId: opp.ghl?.ghlContactId ?? "",
    ghlSynced: opp.ghl?.ghlSyncStatus === "Synced",
    createdAt: opp.ghl?.ghlCreatedAt ?? now,
    updatedAt: opp.ghl?.ghlUpdatedAt ?? now,
    intakeRecord: null,
    communicationLog: { opportunityId: opp.id, entries: [] },
    activeWizardId: null,

    // ── Extended Kanban display fields ──
    industry: opp.industry,
    website: opp.website,
    primaryContact: opp.primaryContact,
    email: opp.email,
    phone: opp.phone,
    affiliateSource: opp.affiliateSource,
    estimatedValue: opp.estimatedValue,
    monthlyValue: opp.monthlyValue,
    contractLength: opp.contractLength,
    probability: opp.probability,
    daysInStage: opp.daysInStage,
    nextAction: opp.nextAction,
    closingMonth: opp.closingMonth,
    opportunityScore: opp.opportunityScore,
    forecastMonth: opp.forecastMonth,
    forecastQuarter: opp.forecastQuarter,

    // ── Sub-feature objects (stored on record, honestly deferred for live queries) ──
    audit: opp.audit ?? NA,
    proposal: opp.proposal ?? NP,
    followUps: opp.followUps ?? [],
    handoff: opp.handoff ?? NH,
    affiliate: opp.affiliate ?? NAff,
    tasks: opp.tasks ?? [],
    notifications: opp.notifications ?? [],
    workflowEvents: opp.workflowEvents ?? [],
    recentActivities: opp.recentActivities ?? [],
    notes: opp.notes ?? [],
    nextSteps: opp.nextSteps ?? [],

    // ── GHL display fields (Coming Soon — stored display data only) ──
    ghl: opp.ghl ?? mkGhl("", "", GHL_PIPELINE_ID, GHL_PIPELINE_NAME, "", "", "", "", "open", 0, "", now, now, now, "Not Connected"),
  };
}

