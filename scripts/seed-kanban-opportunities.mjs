// ── Merge into data file ──────────────────────────────────────────────────────

function readRecords() {
  try {
    const raw = fs.readFileSync(DATA_FILE, "utf-8");
    const parsed = JSON.parse(raw);
    if (!Array.isArray(parsed.records)) throw new Error("bad shape");
    return parsed.records;
  } catch {
    return [];
  }
}

function writeRecords(records) {
  const dir = path.dirname(DATA_FILE);
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(DATA_FILE, JSON.stringify({ records }, null, 2), "utf-8");
}

const existing = readRecords();
const existingIds = new Set(existing.map((r) => r.id));

let added = 0;
let updated = 0;

const merged = [...existing];

for (const opp of KANBAN_OPPORTUNITIES) {
  const record = toRecord(opp);
  const idx = merged.findIndex((r) => r.id === record.id);
  if (idx >= 0) {
    merged[idx] = { ...merged[idx], ...record, updatedAt: new Date().toISOString() };
    updated++;
  } else {
    merged.push(record);
    added++;
  }
}

writeRecords(merged);

