// ── Score Model ───────────────────────────────────────────────────────────────

export interface RenewalScoreBreakdown {
  clientHealth: number;        // 0-100, weight 25%
  projectHealth: number;       // 0-100, weight 20%
  communicationHealth: number; // 0-100, weight 20%
  billingHealth: number;       // 0-100, weight 15%
  callIntelligence: number;    // 0-100, weight 10%
  reportingHealth: number;     // 0-100, weight 5%
  clientEngagement: number;    // 0-100, weight 5%
  overall: number;             // 0-100 computed
}

