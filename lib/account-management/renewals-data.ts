function computeRenewalScore(b: Omit<RenewalScoreBreakdown, "overall">): RenewalScoreBreakdown {
  const overall = Math.round(
    b.clientHealth * 0.25 +
    b.projectHealth * 0.20 +
    b.communicationHealth * 0.20 +
    b.billingHealth * 0.15 +
    b.callIntelligence * 0.10 +
    b.reportingHealth * 0.05 +
    b.clientEngagement * 0.05
  );
  return { ...b, overall };
}

function daysFromNow(isoDate: string): number {
  const now = new Date("2025-07-01");
  const target = new Date(isoDate);
  return Math.round((target.getTime() - now.getTime()) / 86_400_000);
}

